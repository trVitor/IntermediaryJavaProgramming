public class HistoricoPreco {

    private String data;
    private double preco;


    public HistoricoPreco(String data, double preco) {
        this.data = data;
        this.preco = preco;
    }


    public String getData() {
        return data;
    }


    public double getPreco() {
        return preco;
    }
    

    
}
